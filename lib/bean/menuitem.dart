class MenuItem{
  String _id;
  String _en_name;
  String _name;
  String _rank;

  String get id => _id;

  set id(String value) {
    _id = value;
  }

  String get en_name => _en_name;

  String get rank => _rank;

  set rank(String value) {
    _rank = value;
  }

  String get name => _name;

  set name(String value) {
    _name = value;
  }

  set en_name(String value) {
    _en_name = value;
  }


}